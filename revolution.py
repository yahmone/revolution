t.fd(9000/16); t.rt(90)
t.fd(1000); t.rt(90)
t.fd(9000/16)
t.end_fill()
t.penup()
t.rt(90); t.goto(-444, (9000/32)-110)
t.pendown()
t.color("white")
t.begin_fill()
for i in range(5):
  t.fd(70000/330)
  t.rt(144)
t.end_fill()
t.color("red")
